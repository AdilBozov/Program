using System;

class FloatingPointTypes
{
    static void Main()
    {
        float numFlOne = 12.345f;
        float numFlTwo = 3456.091f;
        double numDoOne = 34.567839023;
        double numDOTwo = 8923.1234857;
        Console.WriteLine("Float type  {0} and {1}", numFlOne, numFlTwo);
        Console.WriteLine("Double type {0} and {1}", numDoOne, numDOTwo);
    }
}

